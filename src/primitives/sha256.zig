//! SHA-256 (FIPS 180-4) over std.crypto. Single-shot, the only form the pipeline needs.
const std = @import("std");

pub fn hash(in: []const u8, out: *[32]u8) void {
    std.crypto.hash.sha2.Sha256.hash(in, out, .{});
}

test "sha256 KATs" {
    var out: [32]u8 = undefined;
    hash("abc", &out);
    try std.testing.expectEqualSlices(u8, &[_]u8{
        0xba, 0x78, 0x16, 0xbf, 0x8f, 0x01, 0xcf, 0xea, 0x41, 0x41, 0x40, 0xde, 0x5d, 0xae, 0x22, 0x23,
        0xb0, 0x03, 0x61, 0xa3, 0x96, 0x17, 0x7a, 0x9c, 0xb4, 0x10, 0xff, 0x61, 0xf2, 0x00, 0x15, 0xad,
    }, &out);
    hash("a", &out);
    // SHA256("a") = ca978112... (matches oracle SHA checkpoint for input 'a')
    try std.testing.expectEqualSlices(u8, &[_]u8{
        0xca, 0x97, 0x81, 0x12, 0xca, 0x1b, 0xbd, 0xca, 0xfa, 0xc2, 0x31, 0xb3, 0x9a, 0x23, 0xdc, 0x4d,
        0xa7, 0x86, 0xef, 0xf8, 0x14, 0x7c, 0x4e, 0x72, 0xb9, 0x80, 0x77, 0x85, 0xaf, 0xee, 0x48, 0xbb,
    }, &out);
}
